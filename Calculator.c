#include <stdio.h>
#include <math.h>
int add(int x, int y)
{
    int z = x + y;
    return z;
}
int sub(int a, int b)
{
    int c = a - b;
    return c;
}
int mul(int d, int e)
{
    int f = d * e;
    return f;
}
int div(int g, int h)
{
    int i = g / h;
    return i;
}

double expo(int j, int k)
{
    int l = pow(j, k);
    return l;
}

double sine(int m)
{
    int n = sin(m);
    return n;
}

double cosine(int o)
{
    int p = cos(o);
    return p;
}

double sqroot(int q)
{
    int r = sqrt(q);
    return r;
}